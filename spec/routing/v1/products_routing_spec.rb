require "rails_helper"

RSpec.describe V1::ProductsController, :type => :routing do
  describe "routing" do

    it "routes to #index" do
      expect(:get => "/v1/products").to route_to("v1/products#index")
    end

    it "routes to #new" do
      expect(:get => "/v1/products/new").to route_to("v1/products#new")
    end

    it "routes to #show" do
      expect(:get => "/v1/products/1").to route_to("v1/products#show", :id => "1")
    end

    it "routes to #edit" do
      expect(:get => "/v1/products/1/edit").to route_to("v1/products#edit", :id => "1")
    end

    it "routes to #create" do
      expect(:post => "/v1/products").to route_to("v1/products#create")
    end

    it "routes to #update" do
      expect(:put => "/v1/products/1").to route_to("v1/products#update", :id => "1")
    end

    it "routes to #destroy" do
      expect(:delete => "/v1/products/1").to route_to("v1/products#destroy", :id => "1")
    end

  end
end
